package JavaPracticeWeek3;

public class JavaOperator2 {

    public static void main(String[] args) {

        int A = 10;
        int B = 20;

        System.out.println(A == B);  //return false
        System.out.println(A != B);  //return true
        System.out.println(A > B);   //return false
        System.out.println(A < B);   //return true
        System.out.println(A >= B);  //return false
        System.out.println(A <= B);  //return true
    }
}
