package JavaPracticeWeek3;

public class JavaOperator4 {

    public static void main(String[] args) {

        int A = 10;
        boolean B = true;

        System.out.println(A++); //return should be 11
        System.out.println(++A);
        System.out.println(A--);
        System.out.println(--A);
        System.out.println(!B);  //return false
    }
}
