package JavaPracticeWeek3;

public class JavaOperator3 {

    public static void main(String[] args) {

        int A = 10;

        System.out.println(A<10 && A<20);    //return false
        System.out.println(A<10 || A<20 );  //return true
        System.out.println(!(A<10 && A<10)); //return true
    }
}

